    private void Clic_ClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Clic_ClientesActionPerformed
        String filePath = "clientes.txt";
        File file = new File(filePath);
        
        try {
            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);
            
            DefaultTableModel model = (DefaultTableModel)Tabla1.getModel();
            Object [] lines = br.lines().toArray();
            
            for(int i = 0; i < lines.length; i++){
                String[] row = lines[i].toString().split("-");
                model.addRow(row);
            }
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ListadoClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
        Clic_Clientes.setEnabled(false);
    }//GEN-LAST:event_Clic_ClientesActionPerformed

    private void Tabla1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tabla1MouseClicked

        if(evt.getClickCount() == 1){
            //JOptionPane.showMessageDialog(rootPane, "FILA: " + Tabla1.getSelectedColumn())
            necflis.Info in = new necflis.Info();
            jDesktop.add(in);
            in.show();

            String nombre=String.valueOf(Tabla1.getValueAt(Tabla1.getSelectedRow(),0));

            try{
            FileWriter Writer = new FileWriter("SeleccionarCliente.txt", true);
            Writer.write(nombre);
            Writer.write(System.getProperty("line.separator"));
            Writer.close();
            setVisible(true);
            new NuevoCliente().setVisible(true);
            }
            
            catch (Exception e){
                JOptionPane.showMessageDialog(null,"Error");
            }
        }
    }//GEN-LAST:event_Tabla1MouseClicked
        
    
    
