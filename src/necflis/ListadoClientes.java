    private void Clic_ClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Clic_ClientesActionPerformed
        String filePath = "clientes.txt";
        File file = new File(filePath);
        
        try {
            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);
            
            DefaultTableModel model = (DefaultTableModel)Tabla.getModel();
            Object [] lines = br.lines().toArray();
            
            for(int i = 0; i < lines.length; i++){
                String[] row = lines[i].toString().split("-");
                model.addRow(row);
            }
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ListadoClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
        Clic_Clientes.setEnabled(false);
    }//GEN-LAST:event_Clic_ClientesActionPerformed


